        at(isinstance(cfb0.root_dir_entry, DirEntry))

        rde = cfb0.root_dir_entry
        ae(rde.sid, 0)
        ae(rde.name, "Root Entry")
        ae(rde.type, 5)
        ae(rde.color, 1)
        ae(rde.left_sid, STREAM_ID_NONE)
        ae(rde.right_sid, STREAM_ID_NONE)
        ae(rde.child_sid, 3)
        ae(rde.clsid, UUID("00020906-0000-0000-c000-000000000046"))
        ae(rde.state, 0)
        ae(rde.btime, datetime(1601, 1, 1, 0, 0, 0))
        ae(rde.mtime, filetime_to_datetime(0x01C889F1E1C7A3E0))
        ae(rde.first_sect, 24469)
        ae(rde.size, 128)
