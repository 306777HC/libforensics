    def test__init__(self):
        ae = self.assertEqual
        at = self.assertTrue
        cfb0 = self.cfb0

        ae(cfb0.clsid, UUID(int=0))
        ae(cfb0.ver_minor, 62)
        ae(cfb0.ver_major, 3)
        ae(cfb0.sect_size, 512)
        ae(cfb0.mini_sect_size, 64)
        ae(cfb0.trans_num, 0)
        ae(cfb0.mini_stream_cutoff, 4096)
        at(hasattr(cfb0, "fat"))
